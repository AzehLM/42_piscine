int	ft_str_is_uppercase(char *str)
{
	int	i;

	i = 0;
	if (!str[i])
		return (1);
	while (str[i])
	{
		while (str[i] >= 65 && str[i] <= 90)
			i++;
		if (str[i] != '\0')
			return (0);
	}
	return (1);
}
