#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	j;

	(void) ac;
	i = 1;
	if (ac < 2)
		return (0);
	while (av[i])
	{
		if (av[i] == (void *) '\0')
			return (0);
		j = 0;
		while (av[i][j])
		{
			write(1, &av[i][j], 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
	return (0);
}
